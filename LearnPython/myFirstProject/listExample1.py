animals = ['cat', 'dog', 'rabbit']
print(animals)

# adding an element in list from the end
animals.append('Lion')
print(animals)

# replacing the element at the given index
animals[0] = 'meow cat'
print(animals)

# adding the element at the given index
animals.insert(0, 'tree')

print(animals)

print('*' * 101)

print(animals[0:3])

